// Build a static handle → accreditations map from the CSVs in hotel-lists/.
// Run manually when the source lists change:
//   node scripts/build-accreditations.mjs
// Output: lib/accreditations.generated.ts (committed; read at runtime — no DB, no fs).
//
// Matching is by Instagram handle (exact, normalized), NOT hotel name — every
// source CSV carries an instagram_handle column, so no fuzzy name matching.
// Guardrail: TEXT labels only, never official logos.

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const listsDir = join(here, '..', 'hotel-lists');
const outFile = join(here, '..', 'lib', 'accreditations.generated.ts');

// Minimal RFC-4180-ish CSV parser (handles quoted fields + commas/quotes inside).
function parseCsv(text) {
  const rows = [];
  let row = [], field = '', inQuotes = false;
  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (inQuotes) {
      if (c === '"') {
        if (text[i + 1] === '"') { field += '"'; i++; }
        else inQuotes = false;
      } else field += c;
    } else if (c === '"') inQuotes = true;
    else if (c === ',') { row.push(field); field = ''; }
    else if (c === '\n' || c === '\r') {
      if (c === '\r' && text[i + 1] === '\n') i++;
      if (field !== '' || row.length) { row.push(field); rows.push(row); row = []; field = ''; }
    } else field += c;
  }
  if (field !== '' || row.length) { row.push(field); rows.push(row); }
  return rows;
}

function readRecords(file) {
  const rows = parseCsv(readFileSync(join(listsDir, file), 'utf8'));
  const header = rows[0].map(h => h.trim());
  return rows.slice(1).map(r => Object.fromEntries(header.map((h, i) => [h, (r[i] ?? '').trim()])));
}

const normHandle = (h) => (h || '').trim().toLowerCase().replace(/^@/, '').replace(/\/+$/, '');

// Priority order → the order pills render on a row.
const PRIORITY = [
  'Forbes Five-Star',
  'Condé Nast Gold List',
  'Michelin Three Keys',
  'Michelin Two Keys',
  'Michelin One Key',
];
const MICHELIN = {
  'Three Key': 'Michelin Three Keys',
  'Two Key': 'Michelin Two Keys',
  'One Key': 'Michelin One Key',
};

/** @type {Map<string, Set<string>>} */
const map = new Map();
const add = (handle, label) => {
  const key = normHandle(handle);
  if (!key || !label) return;
  if (!map.has(key)) map.set(key, new Set());
  map.get(key).add(label);
};

for (const r of readRecords('forbes-five-star-hotels.csv')) add(r.instagram_handle, 'Forbes Five-Star');
for (const r of readRecords('conde-nast-gold-list.csv')) add(r.instagram_handle, 'Condé Nast Gold List');
for (const r of readRecords('michelin-keys-uk-ireland.csv')) add(r.instagram_handle, MICHELIN[r.category] ?? null);
// master-luxury-hotels.csv is the enriched combined list — its `source` column
// fills in handles the standalone Forbes/Gold-List files leave blank (esp. Gold
// List: 144 handles here vs 19 standalone). Unioned in; Set dedups overlaps.
for (const r of readRecords('master-luxury-hotels.csv')) {
  const src = r.source || '';
  if (src.includes('Forbes')) add(r.instagram_handle, 'Forbes Five-Star');
  if (src.includes('Gold List')) add(r.instagram_handle, 'Condé Nast Gold List');
}

const sortedKeys = [...map.keys()].sort();
const entries = sortedKeys.map(k => {
  const labels = [...map.get(k)].sort((a, b) => PRIORITY.indexOf(a) - PRIORITY.indexOf(b));
  return `  ${JSON.stringify(k)}: ${JSON.stringify(labels)},`;
});

const out = `// AUTO-GENERATED by scripts/build-accreditations.mjs — DO NOT EDIT BY HAND.
// Source: hotel-lists/*.csv. Regenerate with: node scripts/build-accreditations.mjs
// Maps a normalized Instagram handle (lowercase, no leading @) → accreditation labels.
// Text labels only (no official logos — trademark/endorsement risk).

export const ACCREDITATIONS: Record<string, string[]> = {
${entries.join('\n')}
};
`;

writeFileSync(outFile, out);
console.log(`Wrote ${sortedKeys.length} accredited handles → ${outFile}`);
const counts = { 'Forbes Five-Star': 0, 'Condé Nast Gold List': 0, michelin: 0 };
for (const labels of map.values()) {
  if (labels.has('Forbes Five-Star')) counts['Forbes Five-Star']++;
  if (labels.has('Condé Nast Gold List')) counts['Condé Nast Gold List']++;
  if ([...labels].some(l => l.startsWith('Michelin'))) counts.michelin++;
}
console.log('By list:', counts);
